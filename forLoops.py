for item in 'Python':
    print(item)  # prints all letters in string

for item in ['TestName', 'Test2', 'Test3']:
    print(item)  # prints all strings in array

for item in range(10):
    print(item)  # print 0-9

for item in range(5, 10):
    print(item)  # print 5-9

for item in range(5, 10, 2):  # two steps forward instead of 1
    print(item)